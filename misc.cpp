void autodefuse(CUserCmd* usercmd, IClientEntity* localplayer)
{
	INetChannelInfo *nci = interfaces::engine->GetNetChannelInfo();
	for (int i = 1; i <= 64; i++)
	{
		IClientEntity* m_entity = interfaces::g_entitylist->GetClientEntity(i);
		CCSBomb* bomb = (CCSBomb*)m_entity;
		const auto outgoing_latency = nci->GetLatency(FLOW_OUTGOING);
		const auto countdown = bomb->GetC4BlowTime() - interfaces::Globals->curtime;
		const auto last_time = (localplayer->IsDefusing() ? 5.f : 10.f) + (interfaces::Globals->interval_per_tick * 4 + outgoing_latency);

		if (countdown <= last_time)
			usercmd->buttons |= IN_USE; 
	}
}

